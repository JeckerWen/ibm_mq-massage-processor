package com.wenrj.yc_codecenter.task.entity;

public class LeafPartInfo {
    private String part_code;
    private String part_name;
    private String type_code;
    private String code_gbm;
    private String code_flag;

    public String getPart_code() {
        return part_code;
    }

    public void setPart_code(String part_code) {
        this.part_code = part_code;
    }

    public String getPart_name() {
        return part_name;
    }

    public void setPart_name(String part_name) {
        this.part_name = part_name;
    }

    public String getType_code() {
        return type_code;
    }

    public void setType_code(String type_code) {
        this.type_code = type_code;
    }

    public String getCode_gbm() {
        return code_gbm;
    }

    public void setCode_gbm(String code_gbm) {
        this.code_gbm = code_gbm;
    }

    public String getCode_flag() {
        return code_flag;
    }

    public void setCode_flag(String code_flag) {
        this.code_flag = code_flag;
    }
}
