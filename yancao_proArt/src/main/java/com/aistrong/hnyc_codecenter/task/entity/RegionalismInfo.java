package com.aistrong.hnyc_codecenter.task.entity;

public class RegionalismInfo {
    private String regionalism_code;
    private String regionalism_name;
    private String regionalism_level_class;
    private String regionalism_area;
    private String super_regionalism;
    private String status;

    public String getRegionalism_code() {
        return regionalism_code;
    }

    public void setRegionalism_code(String regionalism_code) {
        this.regionalism_code = regionalism_code;
    }

    public String getRegionalism_name() {
        return regionalism_name;
    }

    public void setRegionalism_name(String regionalism_name) {
        this.regionalism_name = regionalism_name;
    }

    public String getRegionalism_level_class() {
        return regionalism_level_class;
    }

    public void setRegionalism_level_class(String regionalism_level_class) {
        this.regionalism_level_class = regionalism_level_class;
    }

    public String getRegionalism_area() {
        return regionalism_area;
    }

    public void setRegionalism_area(String regionalism_area) {
        this.regionalism_area = regionalism_area;
    }

    public String getSuper_regionalism() {
        return super_regionalism;
    }

    public void setSuper_regionalism(String super_regionalism) {
        this.super_regionalism = super_regionalism;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
